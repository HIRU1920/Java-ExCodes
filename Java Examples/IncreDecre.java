public class IncreDecre
{
	public static void main(String[] args) {
	    
	  // 2. Increment and decrement Operators
	  
	  int i = 89;
	  
	        // Increment
	        
	  System.out.println(i++);  // Value Use 
	  System.out.println(i);    // Increment
	         // Or 
	  System.out.println(++i); // Increment
	  System.out.println(i);   // Value Use 
	  
	  int h = 51; 
	  
	        //Decrement
	        
	 System.out.println(h--); // Value Use 
	 System.out.println(h);   // Decrement
	        // Or
	 System.out.println(--h); // Decrement 
	 System.out.println(h);   // Value Use
	}
}
