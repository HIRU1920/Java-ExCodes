 
public class Operators
{
	public static void main(String[] args) {
	   
	   // Types of operators
	   
	       // 1. Arithmatic operators 
	       
	    // we can use this operators (+,-,/,*),( % is Modulo operators)    
	       
	   int a = 7;
	   int b = 6 + a;   
	   
	   System.out.println(b);
	   
	        // 2. Assignment operators
	        
	    // We can use this Operators (=,+=)
	        
	   int c = 8;
	   c += 4;         
	   
	   System.out.println(c);
	   
	        // 3. Comparison Operators
	        
	     // we can use this Operators (==,>=,<=)  
	       
	   System.out.println(6==8);      // this is  False condition
	   
	   System.out.println(9==9);    // this is  true condition
	                              
	                               
	        // 4. Logical Operators  
	                          
	     // We can use this Operators (&&,||,!)
	                                   
	   System.out.println(9>7 && 7>9); // if one condition is False then whole sentance is False     
	   
	   System.out.println(45>12 && 47>10); // if Both condition are true then whole sentance  is true
	   
	   System.out.println(89>54 || 56>222); // this is a 'OR' Operator... if any one condition is true then whole sentance is true
	   
	   System.out.println(56>65 || 89>98);  // both are False then whole sentance False (we can't put '=' sign)
	   
	   
	        // 5. Bitwise Operators 
	   
	     // we can use this ( &, |)
	     
	     System.out.println(45&23);
	   
	   
	   
	        
	   
	}
}



















